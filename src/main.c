int main(){
    game_state_t state = front_init();

    state = MENU; // Testing. Despues cambiar a MENU
    unsigned int level = 0;

    while(state != CLOSED){
        switch (state){
          case MENU:
            level = 0;
            state = menu();
            break;
          case GAME:
            state = game_update(level++);
            break;
          case GAME_CRAZY:
            break;
          case PAUSE:
            state = game_pause();
            break;
          case CLOSED:
            break;
      }
    }

    endgame();

    return 0;
}
